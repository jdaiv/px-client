export class Vector2 {

    constructor (x = 0, y = 0) {
        this.x = x
        this.y = y
    }

}

export class Vector3 {

    constructor (x = 0, y = 0, z = 0) {
        this.x = x
        this.y = y
        this.z = z
    }

}

export class Rect {

    constructor (x = 0, y = 0, w = 0, h = 0) {
        this.x = x
        this.y = y
        this.w = w
        this.h = h
    }

}